export function useTranslation(language: Language) {
    return (key: string, options?: string | Record<string, string | number>): string => {
        // 1. Trouver la traduction brute
        let text = translations[language][key]

        // 2. Gestion du "defaultValue" (si options est une string)
        if (typeof options === 'string') {
            // Si pas de traduction trouvée, on retourne la valeur par défaut
            if (!text) return options
        }

        // 3. Si toujours pas de texte, on retourne la clé comme fallback ultime
        if (!text) text = key

        // 4. Gestion de l'interpolation (si options est un objet)
        if (options && typeof options === 'object') {
            Object.entries(options).forEach(([k, v]) => {
                // Remplace toutes les occurrences de {key} par value
                text = text.replace(new RegExp(`{${k}}`, 'g'), String(v))
            })
        }

        return text
    }
}